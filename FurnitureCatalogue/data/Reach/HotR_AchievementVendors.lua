function tableMerge(t1, t2)
	if nil == t2 and nil == t1 then 
		return {} 
	elseif nil == t2 then
		return t1
	elseif nil == t1 then
		return t2
	end 
	
    for k,v in pairs(t2) do
		t1[k] = v        
    end
    return t1
end

 
